# Variables en python

int = 10 
float = 3.14
string = "Hola, mundo!"
boolean = True
# Imprimir las variables
print("Variable entera:", int)
print("Variable flotante:", float)  
print("Variable de texto:", string)
print("Variable booleana:", boolean)
# Operaciones con variables
suma = int + float
print("Suma de int y float:", suma)
multiplicacion = int * float
print("Multiplicación de int y float:", multiplicacion)
# Concatenación de strings
saludo = string + " Bienvenidos a Python."
print(saludo)
# Cambiar el valor de una variable
int = 20
print("Nuevo valor de int:", int)
